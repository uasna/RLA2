const makeMatch = (
  id: number, file_name: string, display_map_name: string,
  match_type: string, score_label: string, seconds: number
): MatchItem => ({
  id,
  file_name,
  status: "stable",
  parse_status: "ok",
  parse_level: "header_only",
  meta_status: "ok",
  date: `2026-06-12 11-${String(60 - id * 5).padStart(2,"0")}-00`,
  map_name: display_map_name.replace(/ /g, "_") + "_P",
  match_type,
  team_size: 2,
  team0_score: parseInt(score_label.split("-")[0]),
  team1_score: parseInt(score_label.split("-")[1]),
  total_seconds_played: seconds,
  replay_name: `Match ${id}`,
  score_label,
  seconds_label: `${(seconds / 60).toFixed(1)}`,
  metadata_ready: true,
  display_map_name,
  short_map_name: display_map_name,
  map_family: display_map_name.split(" ")[0],
  map_variant: display_map_name.split(" ").slice(1).join(" ") || null,
  map_name_source: "fallback",
});

